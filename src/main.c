#include "cub3d.h"

int	main(int argc, char **argv)
{
	t_cub	*cub_data;

	if (argc != 2)
		error(USAGE_ERROR, false);
	cub_data = initialization(argv[1]);
	start_game(cub_data);
	free_all(cub_data);
	return (EXIT_SUCCESS);
}
